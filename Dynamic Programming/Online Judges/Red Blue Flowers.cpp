int dp[105][20005];

int depression(int i, int red, int blue){
    if(i == n) return min(red, blue);
    if(dp[i][red] >= blue) return 0;
    dp[i][red] = blue;
    int res = INT_MIN;
    res = max(res, depression(i + 1, red + r[i], blue));
    res = max(res, depression(i + 1, red, blue + b[i]));
    return res;
}

